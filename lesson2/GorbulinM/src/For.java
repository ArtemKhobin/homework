public class For
{
  public static void main(String[] args)
  {

    RegularOutput();
    For();

  }

  public static void RegularOutput()
      {
        System.out.println("*****");
        System.out.println("*****");
        System.out.println("*****");
        System.out.println("*****");
        System.out.println("*****");
        System.out.println();
      }

    public static void For()
    {
      for(int i = 5; i >= 1 ; i--)
      {
        System.out.println("*****");
      }
    }
}
